def convert_currency():
    start_currency = start_currency_combo.get()
    end_currency = end_currency_combo.get()
    amount = amount_entry.get()

    if not start_currency or not end_currency or not amount:
        messagebox.showerror("Error", "Please fill in all fields")
        return

    try:
        amount = float(amount)
        if amount <= 0:
            raise ValueError
    except ValueError:
        messagebox.showerror("Error", "Please enter a valid positive amount")
        return

    url = "https://api.apilayer.com/exchangerates_data/convert"
    payload = {
        "to": end_currency,
        "from": start_currency,
        "amount": amount
    }

    headers = {
        "apikey": "YOUR_API_KEY_HERE"
    }

    response = requests.get(url, headers=headers, params=payload)

    if response.status_code == 200:
        data = response.json()
        if "result" in data:
            result = data["result"]
            start_currency_name = currencies.get(start_currency)
            end_currency_name = currencies.get(end_currency)
            result_label.config(text=f"{amount} {start_currency_name} is equal to {result} {end_currency_name}")
        else:
            messagebox.showerror("Error", "Conversion failed. Error: " + data.get("error", {}).get("info", ""))
    else:
        messagebox.showerror("Error", "Request failed. Status code: " + str(response.status_code))

